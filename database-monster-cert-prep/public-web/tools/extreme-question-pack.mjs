function question({
  id,
  topic,
  prompt,
  choices,
  correct,
  explanation,
  concept,
  difficulty = "final boss",
  type = "scenario",
  source,
  wrong = {},
}) {
  const correctAnswers = Array.isArray(correct) ? correct : [correct];

  return {
    id,
    topic,
    difficulty,
    type: type === "multi_select" ? "multi_select" : type,
    question: prompt,
    choices,
    correct_answer: Array.isArray(correct) ? correct : correctAnswers[0],
    explanation,
    why_wrong_answers: Object.fromEntries(
      choices
        .filter((choice) => !correctAnswers.includes(choice))
        .map((choice) => [
          choice,
          wrong[choice] ?? `This choice does not correctly apply ${concept}.`,
        ]),
    ),
    related_concept: concept,
    recommended_review: [reviewFiles[topic]],
    source_reference: source,
  };
}

